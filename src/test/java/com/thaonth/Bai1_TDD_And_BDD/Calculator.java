package com.thaonth.Bai1_TDD_And_BDD;

public class Calculator {

    public int add(int number1, int number2){
        return (number1 + number2);
    }

    public int sub(int number1, int number2){
        return (number1 - number2);
    }

    public int multi(int number1, int number2){
        return (number1 * number2);
    }
}
